// Mapping is any prescribed way of assigning to each object in one set a particular object in another, based on mathematics theory

const arr = [2, 4, 2, 4];

// Map all values inside type of Array object to `value * 4`
const mapped = arr.map(x => x * 4);

console.log(mapped);
